            ### 关于本站点

            说起来，这个网站一开始设计的初衷是“写一个在线电子简历”。但是在后来找工作的过程中，我发现在线电子简历的作用十分有限，而更多的情况下是：我常用这个站点来介绍自己。不论是职业技能、生活态度、还是二次元，这个站点的内容很多很多。

            一个网站能够表达的信息，总是要比QQ个人资料要丰富的多（更不用说微信的个人资料）。

            于是，在经过6个alpha版本、7个beta版本、1个gamma版本测试迭代后，有了这个gamma.2版本。将侧重点由“电子简历”变为真正的“个人资料”。项目名也由“电子简历（resume）”变为“花生PeA的个人资料（pea3nut-info）”。

            所以，接下来我会从“个人资料”的角度，为这个站点增加更多的内容。

